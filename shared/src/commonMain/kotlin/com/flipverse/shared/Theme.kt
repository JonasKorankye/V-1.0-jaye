)

// Helper function to determine if dark theme should be used based on user preference
@Composable
private fun shouldUseDarkTheme(): Boolean {
    val themePreference = PreferencesRepository.getThemePreference()
    return when (themePreference) {
        "light" -> false
        "dark" -> true
        "system_default" -> isSystemInDarkTheme()
        else -> isSystemInDarkTheme() // fallback to system default
    }
}

@Composable
fun FlipVerseTheme(
    content: @Composable () -> Unit
) {
    val colorScheme = if (shouldUseDarkTheme()) darkScheme else lightScheme

    MaterialTheme(
        colorScheme = colorScheme,
        content = content
    )
}